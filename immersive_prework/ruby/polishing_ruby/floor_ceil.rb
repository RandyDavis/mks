# .floor method examples
# .floor returns the largest integer that is less than or equal to number passed to it.
# This is a good way to round down a float
puts 1.floor     #=> 1
puts 1.2.floor   #=> 1

# .ceil works the opposite way - give it a float and it returns the smallest integer greater or equal to what you passed it
puts 1.ceil          #=> 1
puts 1.2.ceil        #=> 2